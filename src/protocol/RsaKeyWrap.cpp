// Public exponent: 65537 = 0x010001 (3 bytes, big-endian)
static const uint8_t kPublicExp[3] = { 0x01, 0x00, 0x01 };

static std::string Base64Encode(const uint8_t* data, size_t len)
{
    static const char kAlphabet[] =
        "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";

    std::string out;
    out.reserve(((len + 2) / 3) * 4);

    for (size_t i = 0; i < len; i += 3) {
        uint32_t b = static_cast<uint32_t>(data[i]) << 16;
        if (i + 1 < len) b |= static_cast<uint32_t>(data[i + 1]) << 8;
        if (i + 2 < len) b |= static_cast<uint32_t>(data[i + 2]);

        out += kAlphabet[(b >> 18) & 0x3F];
        out += kAlphabet[(b >> 12) & 0x3F];
        out += (i + 1 < len) ? kAlphabet[(b >> 6) & 0x3F] : '=';
        out += (i + 2 < len) ? kAlphabet[b & 0x3F]        : '=';
    }
    return out;
}

std::string RsaKeyWrap::Wrap(const uint8_t key[16])
{
    // Build BCRYPT_RSAKEY_BLOB followed by exponent then modulus
    const ULONG cbExp = static_cast<ULONG>(sizeof(kPublicExp));
    const ULONG cbMod = static_cast<ULONG>(sizeof(kAirPlayModulus));

    const size_t blobSize = sizeof(BCRYPT_RSAKEY_BLOB) + cbExp + cbMod;
    std::vector<uint8_t> blobBuf(blobSize, 0);

    auto* hdr = reinterpret_cast<BCRYPT_RSAKEY_BLOB*>(blobBuf.data());
    hdr->Magic       = BCRYPT_RSAPUBLIC_MAGIC;  // 'RSA1' = 0x31415352
    hdr->BitLength   = 2048;
    hdr->cbPublicExp = cbExp;
    hdr->cbModulus   = cbMod;
    hdr->cbPrime1    = 0;
    hdr->cbPrime2    = 0;

    uint8_t* p = blobBuf.data() + sizeof(BCRYPT_RSAKEY_BLOB);
    std::memcpy(p, kPublicExp, cbExp);
    p += cbExp;
    std::memcpy(p, kAirPlayModulus, cbMod);

    BCRYPT_ALG_HANDLE hAlg  = nullptr;
    BCRYPT_KEY_HANDLE hKey  = nullptr;

    NTSTATUS status = BCryptOpenAlgorithmProvider(
        &hAlg, BCRYPT_RSA_ALGORITHM, nullptr, 0);
    if (!NT_SUCCESS(status))
        return {};

    status = BCryptImportKeyPair(
        hAlg, nullptr,
        BCRYPT_RSAPUBLIC_BLOB,
        &hKey,
        blobBuf.data(), static_cast<ULONG>(blobSize),
        0);
    if (!NT_SUCCESS(status)) {
        BCryptCloseAlgorithmProvider(hAlg, 0);
        return {};
    }

    // Determine output size
    BCRYPT_OAEP_PADDING_INFO oaepInfo = { BCRYPT_SHA1_ALGORITHM };
    ULONG cbCipher = 0;
    status = BCryptEncrypt(
        hKey,
        const_cast<PUCHAR>(key), 16,
        &oaepInfo, nullptr, 0,
        nullptr, 0,
        &cbCipher,
        BCRYPT_PAD_OAEP);
    if (!NT_SUCCESS(status)) {
        BCryptDestroyKey(hKey);
        BCryptCloseAlgorithmProvider(hAlg, 0);
        return {};
    }

    std::vector<uint8_t> ciphertext(cbCipher);
    ULONG cbResult = 0;
    status = BCryptEncrypt(
        hKey,
        const_cast<PUCHAR>(key), 16,
        &oaepInfo, nullptr, 0,
        ciphertext.data(), cbCipher,
        &cbResult,
        BCRYPT_PAD_OAEP);

    BCryptDestroyKey(hKey);
    BCryptCloseAlgorithmProvider(hAlg, 0);

    if (!NT_SUCCESS(status))
        return {};

    return Base64Encode(ciphertext.data(), cbResult);
}
